# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool

# print("7" + 7) # TypeError
print(int("7"), type(int("7")))
print("a" + "b")
print(bool(" "), bool(""))
print(str(221) + "B")
print(float("1e3") + 3.3)
print(type(float("1e3") + 3.3))
