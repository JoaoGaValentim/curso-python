# Tipos int e float
# int -> números inteiros
# O int representa qualquer número postivo ou negativo. int sem sinal
# é considerado positivo
print(7)
print(-7)
print(0)

# float -> Números com pontos flutuantes
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é positivo.
print(7.77, 8.88)
print(0.0, -1.5)

# A função type mostra o tipo que o Python
# inferiu ao valor
print(type(7.77))
print(type(7))
print(type("221B"))
print(type(True))
print(type([]))
print(type({}))
print(type(()))
# obs.: Tudo em Python é objeto
