# Tipo de dados bool (Boolean)
# Ao questionar algo em um programa,
# só existem duas respostas possíveis:
# sim (True) ou n±ao (False).
# Existem vários operadores para "questinoar"
# Dentre eles o ==, que é um operador lógico que
# questiona se um valor é igual ao outro.

print(7 == 7)  # True
print(7 == 8)  # False
print(type(7 == 7))  # False
